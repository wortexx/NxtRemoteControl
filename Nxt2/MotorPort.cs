namespace Nxt2
{
	/// <summary>
	/// Available motor ports.
	/// </summary>
	public enum MotorPort
	{
		/// <summary>
		/// Motor connected to port A.
		/// </summary>
		A = 0,

		/// <summary>
		/// Motor connected to port B.
		/// </summary>
		B = 1,

		/// <summary>
		/// Motor connected to port C.
		/// </summary>
		C = 2,

		/// <summary>
		/// All motors connected to A, B and C ports.
		/// </summary>
		All = 0xFF
	}
}
